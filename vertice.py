class Vertice(object):
	def __init__(self, name, line, column, block, color = -1):
		self.name = name
		self.edges = []
		self.grau = 0
		self.line = line
		self.column = column
		self.block = block
		self.color = color

	def adjacente(n):
		self.edges.append(n)
		self.grau += 1

	def colorir(color):
		self.color = color
